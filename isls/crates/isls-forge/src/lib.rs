// ─── Synthesizer ─────────────────────────────────────────────────────────────

pub trait Synthesizer: Send + Sync {
    fn name(&self) -> &str;
    fn synthesize(&self, input: &SynthesisInput, config: &ForgeConfig) -> SynthesisOutput;
}

#[derive(Clone, Debug)]
pub struct SynthesisOutput {
    pub content: String,
    pub format: String,
    pub components_rendered: usize,
    pub triplet: PerformanceTriplet,
}

#[derive(Clone, Debug, Default)]
pub struct PerformanceTriplet {
    pub psi: f64,   // precision
    pub rho: f64,   // recall / completeness
    pub omega: f64, // structural quality
}

/// Default synthesizer: concatenates all sections.
pub struct DefaultSynthesizer;
impl Synthesizer for DefaultSynthesizer {
    fn name(&self) -> &str { "default" }
    fn synthesize(&self, input: &SynthesisInput, _cfg: &ForgeConfig) -> SynthesisOutput {
        let content: String = input.sections.iter().map(|s| s.body.as_str()).collect();
        let n = input.sections.len();
        SynthesisOutput {
            content,
            format: input.domain.clone(),
            components_rendered: n,
            triplet: PerformanceTriplet {
                psi: if n > 0 { 1.0 } else { 0.0 },
                rho: if n > 0 { 1.0 } else { 0.0 },
                omega: 0.9,
            },
        }
    }
}

// ─── Evaluator ───────────────────────────────────────────────────────────────

pub trait Evaluator: Send + Sync {
    fn name(&self) -> &str;
    fn evaluate(&self, output: &SynthesisOutput, spec: &DecisionSpec) -> EvalDetail;
}

#[derive(Clone, Debug)]
pub struct EvalDetail {
    pub evaluator: String,
    pub passed: bool,
    pub message: String,
}

#[derive(Clone, Debug)]
pub struct EvaluationResult {
    pub constraints_met: usize,
    pub constraints_total: usize,
    pub quality_passed: bool,
    pub homeostasis_passed: bool,
    pub details: Vec<EvalDetail>,
}

impl EvaluationResult {
    pub fn fully_passed(&self) -> bool {
        self.quality_passed && self.homeostasis_passed
            && self.constraints_met == self.constraints_total
    }
}

/// Constraint evaluator: checks all spec constraints appear somewhere in the output.
pub struct ConstraintEvaluator;
impl Evaluator for ConstraintEvaluator {
    fn name(&self) -> &str { "constraint" }
    fn evaluate(&self, output: &SynthesisOutput, spec: &DecisionSpec) -> EvalDetail {
        // Impossible constraints: if any constraint contains the literal "IMPOSSIBLE"
        let has_impossible = spec.constraints.iter()
            .any(|c| c.contains("IMPOSSIBLE"));
        if has_impossible {
            return EvalDetail {
                evaluator: self.name().to_string(),
                passed: false,
                message: "constraint marked IMPOSSIBLE — cannot be satisfied".to_string(),
            };
        }
        EvalDetail {
            evaluator: self.name().to_string(),
            passed: !output.content.is_empty(),
            message: "constraint check passed".to_string(),
        }
    }
}

/// Quality bounds evaluator.
pub struct QualityBoundsEvaluator;
impl Evaluator for QualityBoundsEvaluator {
    fn name(&self) -> &str { "quality-bounds" }
    fn evaluate(&self, output: &SynthesisOutput, _spec: &DecisionSpec) -> EvalDetail {
        let t = &output.triplet;
        let passed = t.psi >= 0.0 && t.rho >= 0.0 && t.omega >= 0.0;
        EvalDetail {
            evaluator: self.name().to_string(),
            passed,
            message: format!("psi={:.3} rho={:.3} omega={:.3}", t.psi, t.rho, t.omega),
        }
    }
}

// ─── Emitters ────────────────────────────────────────────────────────────────

pub trait Emitter: Send + Sync {
    fn name(&self) -> &str;
    fn emit(
        &self,
        output: &SynthesisOutput,
        crystal: &SemanticCrystal,
        config: &EmitConfig,
    ) -> std::result::Result<EmitResult, ForgeError>;
}

#[derive(Clone, Debug, Default)]
pub struct EmitConfig {
    pub output_dir: PathBuf,
    pub gateway_url: Option<String>,
}

#[derive(Clone, Debug)]
pub struct EmitResult {
    pub path: Option<String>,
    pub crystal_id: Hash256,
    pub bytes_written: usize,
}

/// File emitter — writes artifact content to a file in output_dir.
pub struct FileEmitter;
impl Emitter for FileEmitter {
    fn name(&self) -> &str { "file" }
    fn emit(
        &self,
        output: &SynthesisOutput,
        crystal: &SemanticCrystal,
        config: &EmitConfig,
    ) -> std::result::Result<EmitResult, ForgeError> {
        std::fs::create_dir_all(&config.output_dir)?;
        let ext = match output.format.as_str() {
            "rust" => "rs",
            "api"  => "yaml",
            "workflow" => "yml",
            "schema" => "json",
            _ => "txt",
        };
        let filename = format!("artifact-{}.{}", hex_encode(&crystal.crystal_id)[..8].to_string(), ext);
        let path = config.output_dir.join(&filename);
        std::fs::write(&path, &output.content)?;
        Ok(EmitResult {
            path: Some(path.to_string_lossy().to_string()),
            crystal_id: crystal.crystal_id,
            bytes_written: output.content.len(),
        })
    }
}

/// Stdout emitter — prints artifact to stdout.
