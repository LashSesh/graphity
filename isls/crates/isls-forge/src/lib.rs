// ─── Error ───────────────────────────────────────────────────────────────────

#[derive(Debug, Error)]
pub enum ForgeError {
    #[error("no monoliths produced by PMHD drill — check DecisionSpec or thresholds")]
    NoMonoliths,
    #[error("matrix '{0}' not registered")]
    UnknownMatrix(String),
    #[error("synthesizer '{0}' not registered")]
    UnknownSynthesizer(String),
    #[error("evaluation failed: {0}")]
    EvaluationFailed(String),
    #[error("IR error: {0}")]
    Ir(#[from] IrError),
    #[error("store error: {0}")]
    Store(#[from] StoreError),
    #[error("io error: {0}")]
    Io(#[from] std::io::Error),
    #[error("serialization: {0}")]
    Serde(#[from] serde_json::Error),
}

pub type Result<T> = std::result::Result<T, ForgeError>;

// ─── Helpers ─────────────────────────────────────────────────────────────────

fn hex_encode(bytes: &[u8]) -> String {
    bytes.iter().map(|b| format!("{:02x}", b)).collect()
}

// ─── Matrix (domain interpreter) ─────────────────────────────────────────────

/// Domain-specific interpreter that maps ArtifactIR components to concrete structures.
pub trait Matrix: Send + Sync {
    fn name(&self) -> &str;
    fn domain(&self) -> &str;
    fn interpret(&self, ir: &ArtifactIR, config: &MatrixConfig) -> SynthesisInput;
    fn supported_kinds(&self) -> Vec<String>;
}

#[derive(Clone, Debug)]
pub struct MatrixConfig {
    pub indent: usize,
    pub extra: BTreeMap<String, String>,
}

impl Default for MatrixConfig {
    fn default() -> Self {
        Self { indent: 4, extra: BTreeMap::new() }
    }
}

/// Synthesis input produced by a matrix
#[derive(Clone, Debug)]
pub struct SynthesisInput {
    pub domain: String,
    pub sections: Vec<SynthesisSection>,
}

#[derive(Clone, Debug)]
pub struct SynthesisSection {
    pub kind: String,
    pub name: String,
    pub body: String,
}

// ─── Built-in Matrices ────────────────────────────────────────────────────────

pub struct RustModuleMatrix;
impl Matrix for RustModuleMatrix {
    fn name(&self) -> &str { "rust-module" }
    fn domain(&self) -> &str { "rust" }
    fn supported_kinds(&self) -> Vec<String> {
        vec!["claim".into(), "assumption".into(), "function".into(), "type".into()]
    }
    fn interpret(&self, ir: &ArtifactIR, _cfg: &MatrixConfig) -> SynthesisInput {
        let mut sections = Vec::new();
        sections.push(SynthesisSection {
            kind: "module-header".into(),
            name: "header".into(),
            body: format!(
                "// Generated by isls-forge v1.0.0\n// Domain: {}\n// Artifact: {}\n",
                ir.header.domain,
                hex_encode(&ir.header.artifact_id)
            ),
        });
        for comp in &ir.components {
            let body = match comp.kind.as_str() {
                "claim" => format!("/// {}\npub mod artifact {{\n    // core claim\n}}\n", comp.content),
                "assumption" => format!("/// Assumption: {}\n", comp.content),
                _ => format!("// {}: {}\n", comp.kind, comp.name),
            };
            sections.push(SynthesisSection { kind: comp.kind.clone(), name: comp.name.clone(), body });
        }
        SynthesisInput { domain: "rust".into(), sections }
    }
}

