/// Gateway emitter — prepares an HTTP-ready payload. Does not perform I/O in tests.
pub struct GatewayEmitter;
impl Emitter for GatewayEmitter {
    fn name(&self) -> &str { "gateway" }
    fn emit(
        &self,
        output: &SynthesisOutput,
        crystal: &SemanticCrystal,
        config: &EmitConfig,
    ) -> std::result::Result<EmitResult, ForgeError> {
        // Build the payload (gateway not available in unit test context; log the URL).
        let _url = config.gateway_url.as_deref().unwrap_or("http://localhost:8080");
        // In production, POST payload to gateway. Here we record bytes_written only.
        let payload = serde_json::json!({
            "crystal_id": hex_encode(&crystal.crystal_id),
            "domain": output.format,
            "content_length": output.content.len(),
        });
        let bytes = payload.to_string().len();
        Ok(EmitResult { path: None, crystal_id: crystal.crystal_id, bytes_written: bytes })
    }
}

