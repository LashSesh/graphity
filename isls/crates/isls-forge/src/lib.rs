// ─── Persistent PatternMemory (store-backed) ──────────────────────────────────

/// Store-backed pattern memory for the forge.
pub struct ForgePatternMemory {
    inner: PatternMemory,
}

impl ForgePatternMemory {
    pub fn load(store: &IslandStore) -> Result<Self> {
        let rows = store.list_patterns(None)?;
        let mut entries = Vec::new();
        for row in rows {
            let quality: isls_pmhd::QualityMetrics =
                serde_json::from_str(&row.quality_json)?;
            let signature: FiveDState =
                serde_json::from_str(&row.signature_json)?;
            let kinds: Vec<String> =
                serde_json::from_str(&row.component_kinds)?;
            entries.push(PatternEntry {
                monolith_id: row.monolith_id,
                domain: row.domain,
                quality,
                signature,
                component_kinds: kinds,
                timestamp: row.timestamp,
            });
        }
        Ok(Self { inner: PatternMemory::from_entries(entries) })
    }

    pub fn persist(&self, entry: &PatternEntry, store: &IslandStore) -> Result<()> {
        let row = PatternRow {
            id: {
                let mut h = Sha256::new();
                h.update(entry.monolith_id.as_bytes());
                h.update(entry.domain.as_bytes());
                hex_encode(&h.finalize())
            },
            monolith_id: entry.monolith_id.clone(),
            domain: entry.domain.clone(),
            quality_json: serde_json::to_string(&entry.quality)?,
            signature_json: serde_json::to_string(&entry.signature)?,
            component_kinds: serde_json::to_string(&entry.component_kinds)?,
            timestamp: entry.timestamp,
        };
        store.insert_pattern(&row)?;
        Ok(())
    }

    pub fn memory(&self) -> &PatternMemory { &self.inner }
    pub fn len(&self) -> usize { self.inner.len() }
}

// ─── ForgeEngine ─────────────────────────────────────────────────────────────

pub struct ForgeEngine {
    matrices: BTreeMap<String, Box<dyn Matrix>>,
    synthesizers: BTreeMap<String, Box<dyn Synthesizer>>,
    evaluators: Vec<Box<dyn Evaluator>>,
    emitters: Vec<Box<dyn Emitter>>,
    config: ForgeConfig,
    pattern_memory: PatternMemory,
}

impl ForgeEngine {
    pub fn new(config: ForgeConfig) -> Self {
        let mut matrices: BTreeMap<String, Box<dyn Matrix>> = BTreeMap::new();
        matrices.insert("rust-module".into(), Box::new(RustModuleMatrix));
        matrices.insert("http-api".into(), Box::new(HttpApiMatrix));
        matrices.insert("workflow".into(), Box::new(WorkflowMatrix));
        matrices.insert("schema".into(), Box::new(SchemaMatrix));

        let mut synthesizers: BTreeMap<String, Box<dyn Synthesizer>> = BTreeMap::new();
        synthesizers.insert("default".into(), Box::new(DefaultSynthesizer));

        let evaluators: Vec<Box<dyn Evaluator>> = vec![
            Box::new(ConstraintEvaluator),
            Box::new(QualityBoundsEvaluator),
        ];
        let emitters: Vec<Box<dyn Emitter>> = vec![
            Box::new(FileEmitter),
            Box::new(StdoutEmitter),
            Box::new(GatewayEmitter),
        ];

        Self {
            matrices,
            synthesizers,
            evaluators,
            emitters,
            config,
            pattern_memory: PatternMemory::new(),
        }
    }

    pub fn with_store(config: ForgeConfig, store: &IslandStore) -> Result<Self> {
        let mut eng = Self::new(config);
        let fpm = ForgePatternMemory::load(store)?;
        eng.pattern_memory = fpm.inner;
        Ok(eng)
    }

    pub fn register_matrix(&mut self, m: Box<dyn Matrix>) {
        self.matrices.insert(m.name().to_string(), m);
    }

    pub fn register_synthesizer(&mut self, s: Box<dyn Synthesizer>) {
        self.synthesizers.insert(s.name().to_string(), s);
    }

    pub fn register_emitter(&mut self, e: Box<dyn Emitter>) {
        self.emitters.push(e);
    }

    /// Core forge pipeline: drill → IR → matrix → synthesize → evaluate → crystal → emit.
    pub fn forge(&mut self, spec: DecisionSpec) -> Result<ForgeResult> {
        // Step 1: PMHD drill
        let drill_cfg = spec.config.clone();
        let memory = self.pattern_memory.clone();
        let mut drill = DrillEngine::with_memory(drill_cfg, memory);
        let drill_result = drill.drill(&spec);

        if drill_result.monoliths.is_empty() {
            return Err(ForgeError::NoMonoliths);
        }

        // Merge new pattern memory entries back
        for entry in drill.pattern_memory().entries() {
            self.pattern_memory.add(entry.clone());
        }

        // Step 2: For each monolith → IR → matrix → synthesize → evaluate → crystal → emit
        let matrix_name = self.config.matrix.clone();
        let matrix = self.matrices.get(&matrix_name)
            .ok_or_else(|| ForgeError::UnknownMatrix(matrix_name.clone()))?;

        let synth_name = self.config.synth.clone();
        let synthesizer = self.synthesizers.get(&synth_name)
            .ok_or_else(|| ForgeError::UnknownSynthesizer(synth_name.clone()))?;

        let mut artifacts = Vec::new();
        let mut crystals = Vec::new();
        let mut combined_stats = PropagationStats::default();

        for (i, monolith) in drill_result.monoliths.iter().enumerate() {
            let ir = ArtifactIR::build_from_monolith(monolith, &spec, i as u32)?;

            // Constraint Propagation Pass — runs BEFORE Oracle (C25).
            // Classifies components and determines synthesis strategy to reduce Oracle calls.
            let (_analyses, cp_stats) = run_propagation_pass(&ir, &self.pattern_memory);
            combined_stats.total        += cp_stats.total;
            combined_stats.deterministic += cp_stats.deterministic;
            combined_stats.pattern_reuse += cp_stats.pattern_reuse;
            combined_stats.constrained  += cp_stats.constrained;
            combined_stats.open         += cp_stats.open;

            // Matrix interpret
            let matrix_cfg = MatrixConfig::default();
            let synth_input = matrix.interpret(&ir, &matrix_cfg);

            // Synthesize
            let synth_output = synthesizer.synthesize(&synth_input, &self.config);

            // Evaluate
            let evaluation = self.run_evaluators(&synth_output, &spec);

            // Build forge crystal
            let crystal = build_forge_crystal(&ir, &spec, &synth_output);
            let crystal_id = crystal.crystal_id;

            // Emit (only active emitters from config)
            let emit_cfg = EmitConfig {
                output_dir: self.config.output_dir.clone(),
                gateway_url: self.config.gateway_url.clone(),
            };
            let mut emit_results = Vec::new();
            for emitter in &self.emitters {
                if self.config.emit.contains(&emitter.name().to_string()) {
                    if let Ok(er) = emitter.emit(&synth_output, &crystal, &emit_cfg) {
                        emit_results.push(er);
                    }
                }
            }

            artifacts.push(ForgeArtifact { ir, synthesis: synth_output, evaluation, crystal_id, emit_results });
            crystals.push(crystal);
        }

        // Manifest
        let crystal_ids: Vec<Hash256> = crystals.iter().map(|c| c.crystal_id).collect();
        let manifest = build_forge_manifest(&spec, &crystal_ids, self.pattern_memory.len());

        Ok(ForgeResult {
            artifacts,
            crystals,
            manifest,
            pattern_count: self.pattern_memory.len(),
            propagation_stats: combined_stats,
        })
    }

    /// Forge from an existing SemanticCrystal (analytical discovery → forge).
    pub fn forge_from_crystal(
        &mut self,
        crystal: &SemanticCrystal,
        domain: &str,
    ) -> Result<ForgeResult> {
        // Build a DecisionSpec derived from the crystal's topology signature
        let mut goals = BTreeMap::new();
        goals.insert("coherence".to_string(), crystal.stability_score);
        goals.insert("quality".to_string(), 1.0 - crystal.free_energy);

        let spec = DecisionSpec::new(
            format!("Forge artifact from crystal {} (domain: {domain})",
                hex_encode(&crystal.crystal_id)[..8].to_string()),
            goals,
            Vec::new(),
            domain,
            self.config.pmhd.clone(),
        );
        self.forge(spec)
    }

    fn run_evaluators(
        &self,
        output: &SynthesisOutput,
        spec: &DecisionSpec,
    ) -> EvaluationResult {
        let details: Vec<EvalDetail> = self.evaluators.iter()
            .map(|ev| ev.evaluate(output, spec))
            .collect();
        let quality_passed = details.iter().filter(|d| d.evaluator == "quality-bounds").all(|d| d.passed);
        let homeostasis_passed = !output.content.is_empty();
        let constraints_total = spec.constraints.len().max(1); // at least 1
        let constraints_met = details.iter().filter(|d| d.evaluator == "constraint" && d.passed).count();
        EvaluationResult { constraints_met, constraints_total, quality_passed, homeostasis_passed, details }
    }

    pub fn pattern_memory(&self) -> &PatternMemory { &self.pattern_memory }
    pub fn list_matrices(&self) -> Vec<&str> { self.matrices.keys().map(|k| k.as_str()).collect() }
}

// ─── Crystal Builder ─────────────────────────────────────────────────────────

fn build_forge_crystal(
    ir: &ArtifactIR,
    spec: &DecisionSpec,
    _output: &SynthesisOutput,
) -> SemanticCrystal {
    // Topology from component dependency graph
    let n_comp = ir.components.len();
    let n_iface = ir.interfaces.len();
    let topology_sig = TopologySignature {
        betti_0: n_comp as u64,
        betti_1: n_iface as u64,
        betti_2: 0,
        spectral_gap: ir.metrics.quality_score,
        euler_char: n_comp as i64 - n_iface as i64,
        cheeger_estimate: ir.metrics.coherence,
        kuramoto_coherence: ir.metrics.stability,
        mean_propagation_time: if n_comp > 0 { 1.0 / n_comp as f64 } else { 0.0 },
        dtl_connected: n_comp > 0,
    };

    // Evidence chain entry from IR provenance
    let prov_bytes = serde_json::to_vec(&ir.provenance).unwrap_or_default();
    let ev_digest = content_address_raw(&prov_bytes);
    let ev_entry = EvidenceEntry {
        digest: ev_digest,
        content: prov_bytes,
        provenance: ProvenanceEnvelope {
            origin: format!("forge:{}", spec.domain),
            chain: vec![hex_encode(&ir.header.artifact_id)],
            sig: None,
        },
        prev: None,
    };

    // Content-address the crystal core (crystal_id)
    #[derive(Serialize)]
    struct CrystalCore<'a> {
        artifact_id: &'a Hash256,
        spec_id: &'a Hash256,
        n_comp: usize,
        quality_score: f64,
        program_id: &'a str,
    }
    let prog_id = format!("forge:{}", hex_encode(&spec.id));
    let core = CrystalCore {
        artifact_id: &ir.header.artifact_id,
        spec_id: &spec.id,
        n_comp,
        quality_score: ir.metrics.quality_score,
        program_id: &prog_id,
    };
    let crystal_id = content_address(&core);

    let stability = ir.metrics.quality_score.clamp(0.0, 1.0);
    let free_energy = (1.0 - stability).max(0.0);

    let mut operator_versions = BTreeMap::new();
    operator_versions.insert("isls-forge".to_string(), "1.0.0".to_string());
    operator_versions.insert("isls-pmhd".to_string(), "1.0.0".to_string());
    operator_versions.insert("isls-artifact-ir".to_string(), "1.0.0".to_string());

    let commit_proof = CommitProof {
        evidence_digests: vec![ev_digest],
        operator_stack: operator_versions.iter()
            .map(|(k, v)| (k.clone(), v.clone()))
            .collect(),
        gate_values: GateSnapshot {
            d: stability,
            q: ir.metrics.coherence,
            r: ir.metrics.robustness,
            g: ir.metrics.coverage,
            j: ir.metrics.stability,
            p: stability,
            n: ir.metrics.novelty,
            k: ir.metrics.diversity,
            kairos: stability > 0.5,
        },
        structural_result: n_comp > 0,
        consensus_result: ConsensusResult {
            primal_score: stability,
            dual_score: stability,
            mci: stability,
            threshold: 0.5,
        },
        por_trace: PoRTrace {
            search_enter: 0.0,
            lock_enter: Some(1.0),
            verify_enter: Some(2.0),
            commit_enter: Some(3.0),
        },
        carrier_id: 0,
        carrier_offset: 0.0,
    };

    SemanticCrystal {
        crystal_id,
        region: (0..n_comp as u64).collect(),
        constraint_program: Vec::new(),
        stability_score: stability,
        topology_signature: topology_sig,
        betti_numbers: vec![n_comp as u64, n_iface as u64, 0],
        evidence_chain: vec![ev_entry],
        commit_proof,
        operator_versions,
        created_at: ir.header.timestamp_tick,
        free_energy,
        carrier_instance_idx: 0,
        scale_tag: format!("forge:{}", spec.domain),
        universe_id: format!("forge:{}", hex_encode(&spec.id)[..8].to_string()),
        sub_crystal_ids: Vec::new(),
        parent_crystal_ids: Vec::new(),
        genesis_metadata: None,
    }
}

fn build_forge_manifest(
    spec: &DecisionSpec,
    crystal_ids: &[Hash256],
    pattern_count: usize,
) -> ForgeManifest {
    #[derive(Serialize)]
    struct ManifestCore<'a> {
        spec_id: &'a Hash256,
        crystal_count: usize,
        pattern_count: usize,
    }
    let core = ManifestCore {
        spec_id: &spec.id,
        crystal_count: crystal_ids.len(),
        pattern_count,
    };
    let run_id = content_address(&core);
    let program_id = format!("forge:{}", hex_encode(&spec.id));
    ForgeManifest {
        run_id,
        spec_id: spec.id,
        program_id,
        crystal_ids: crystal_ids.to_vec(),
        monolith_count: crystal_ids.len(),
        pattern_count,
        timestamp: 0.0,
    }
}

